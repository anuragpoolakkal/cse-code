ASSUME CS:CODE DS:DATA

DATA SEGMENT
NUM1 DW 08
NUM2 DW 16
LCM DW 2 DUP(0)
DATA ENDS
CODE SEGMENT

START:  MOV AX,DATA
	MOV DS,AX
	XOR AX,AX
	MOV AX,NUM1
	MOV BX,NUM2
	MOV DX,00
	JE FINDHCF
	MOV AX,BX
	MOV AX,00
	JMP RLOOP
	FINDHCF: MOV CH,BX
	XOR AX,AX
	XOR BX,BX
	MOV AL,NUM1
	MOV BL,NUM2
	MUL BX
	MOV BL,CH
	MOV BH,00
	DIV BX
	MOV CL,AL
	XOR BL,CL
	CALL DISPLAY
	MOV AH,4CH
	INT 21H
